package com.java.service.error;

/**
 * Типы ошибок базы данных для понятного отображения пользователям.
 */
public enum DatabaseErrorType {
    /**
     * Значение превышает максимальную длину поля
     * Пример: строка длиной 300 символов в поле VARCHAR(255)
     */
    VALUE_TOO_LONG,

    /**
     * Общее нарушение ограничений БД
     */
    CONSTRAINT_VIOLATION,

    /**
     * Нарушение внешнего ключа
     * Пример: ссылка на несуществующую запись
     */
    FOREIGN_KEY_VIOLATION,

    /**
     * Дубликат уникального значения
     * Пример: попытка вставить существующий email
     */
    UNIQUE_VIOLATION,

    /**
     * NULL в обязательном поле
     * Пример: отсутствие значения в поле с NOT NULL
     */
    NOT_NULL_VIOLATION,

    /**
     * Неизвестный тип ошибки (fallback)
     */
    UNKNOWN
}
